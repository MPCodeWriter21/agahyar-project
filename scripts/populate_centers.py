def populate() -> int:
    """Create or update ServiceCenter records.

    Returns the number of records created or updated.
    """
    count = 0
    for entry in SERVICE_CENTERS:
        service_name = entry["service_name"]
        try:
            service = Service.objects.get(name=service_name)
        except Service.DoesNotExist:
            print(
                f'  [  SKIP] Service "{service_name}" not found. Run populate_services.py first.'
            )
            continue

        for center_data in entry["centers"]:
            city = center_data["city"]
            name = center_data["name"]
            obj, created = ServiceCenter.objects.update_or_create(
                name=name,
                city=city,
                defaults={
                    "service": service,
                    "address": center_data["address"],
                    "phone": center_data["phone"],
                },
            )
            count += 1
            action = " created" if created else " updated"
            print(f"  [{action.strip():>7}] {obj}")

    return count


def main() -> None:
    total_centers = sum(len(e["centers"]) for e in SERVICE_CENTERS)
    print(f"Populating database with {total_centers} ServiceCenter records ...\n")
    count = populate()
    print(f"\nDone. {count} center(s) processed.")


if __name__ == "__main__":
    main()
