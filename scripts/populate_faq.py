def populate() -> int:
    """Create or update FAQ records.

    Returns the number of records created or updated.
    """
    count = 0
    for data in FAQS:
        question = data.pop("question")
        answer = data.pop("answer")
        obj, created = FAQ.objects.update_or_create(
            question=question,
            defaults={"answer": answer, **data},
        )
        count += 1
        action = " created" if created else " updated"
        print(f"  [{action.strip():>7}] {obj}")

    return count


def main() -> None:
    print(f"Populating database with {len(FAQS)} FAQ records ...\n")
    count = populate()
    print(f"\nDone. {count} FAQ(s) processed.")


if __name__ == "__main__":
    main()
