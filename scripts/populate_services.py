def populate_services() -> int:
    """Create or update Service records from the SERVICES list."""
    count = 0
    for data in SERVICES:
        name = data.pop("name")
        keywords = data.pop("keywords")
        obj, created = Service.objects.update_or_create(
            name=name,
            defaults={**data, "keywords": keywords},
        )
        count += 1
        action = " created" if created else " updated"
        print(f"  [{action.strip():>7}] {obj.name}")
    return count


def populate_centers() -> int:
    """Create or update ServiceCenter records."""
    count = 0
    for entry in SERVICE_CENTERS:
        service_name = entry["service_name"]
        try:
            service = Service.objects.get(name=service_name)
        except Service.DoesNotExist:
            print(f'  [  SKIP] Service "{service_name}" not found.')
            continue

        service_wh = entry.get("working_hours", "")
        for center_data in entry["centers"]:
            city = center_data["city"]
            name = center_data["name"]
            coord_str = center_data.get("coordinate", "")
            if coord_str:
                lat, lng = coord_str.split(",")
                coordinate = Point(float(lng), float(lat), srid=4326)
            else:
                coordinate = None
            obj, created = ServiceCenter.objects.update_or_create(
                name=name,
                city=city,
                defaults={
                    "service": service,
                    "address": center_data["address"],
                    "working_hours": center_data.get("working_hours", service_wh),
                    "postal_code": center_data.get("postal_code", ""),
                    "coordinate": coordinate,
                },
            )
            count += 1
            action = " created" if created else " updated"
            print(f"  [{action.strip():>7}] {obj}")

            phone_number = center_data.get("phone", "")
            if phone_number:
                phone_obj, phone_created = ServiceCenterPhone.objects.update_or_create(
                    center=obj,
                    label="main",
                    defaults={
                        "phone": phone_number,
                        "order": 0,
                    },
                )
                if phone_created:
                    print(f"    [  added] phone: {phone_number}")

    return count


def main() -> None:
    print(f"Populating database with {len(SERVICES)} service records ...\n")
    svc_count = populate_services()
    print(f"\nDone. {svc_count} service(s) processed.\n")

    total_centers = sum(len(e["centers"]) for e in SERVICE_CENTERS)
    print(f"Populating database with {total_centers} ServiceCenter records ...\n")
    center_count = populate_centers()
    print(f"\nDone. {center_count} center(s) processed.")


if __name__ == "__main__":
    main()
