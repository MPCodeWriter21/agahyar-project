def populate() -> int:
    """Create or update Service records from the SERVICES list.

    Returns the number of records created or updated.
    """
    count = 0
    for data in SERVICES:
        name = data.pop("name")
        keywords = data.pop("keywords")
        obj, created = Service.objects.update_or_create(
            name=name,
            defaults={**data, "keywords": keywords},
        )
        count += 1
        action = " created" if created else " updated"
        print(f"  [{action.strip():>7}] {obj.name}")
    return count


def main() -> None:
    print(f"Populating database with {len(SERVICES)} service records ...\n")
    count = populate()
    print(f"\nDone. {count} service(s) processed.")


if __name__ == "__main__":
    main()
